package entity;

public class Customer {
private long C_Id;			//客戶編號
private String C_Name;		//客戶名稱
private String C_Phone;		//客戶電話
private String C_Cellphone; //客戶手機
private String C_Email;		//客戶信箱
private String C_Address;	//客戶地址
private String C_Account;   //登入帳號
private String C_Password;	//登入密碼
public long getC_Id() {
	return C_Id;
}
public void setC_Id(long c_Id) {
	C_Id = c_Id;
}
public String getC_Name() {
	return C_Name;
}
public void setC_Name(String c_Name) {
	C_Name = c_Name;
}
public String getC_Phone() {
	return C_Phone;
}
public void setC_Phone(String c_Phone) {
	C_Phone = c_Phone;
}
public String getC_Cellphone() {
	return C_Cellphone;
}
public void setC_Cellphone(String c_Cellphone) {
	C_Cellphone = c_Cellphone;
}
public String getC_Email() {
	return C_Email;
}
public void setC_Email(String c_Email) {
	C_Email = c_Email;
}
public String getC_Address() {
	return C_Address;
}
public void setC_Address(String c_Address) {
	C_Address = c_Address;
}
public String getC_Account() {
	return C_Account;
}
public void setC_Account(String c_Account) {
	C_Account = c_Account;
}
public String getC_Password() {
	return C_Password;
}
public void setC_Password(String c_Password) {
	C_Password = c_Password;
}


}
